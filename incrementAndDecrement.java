public class incrementAndDecrement {
  public static void main(String []args) {
      // post increment and pre increment
      int x=20;
      System.out.println(x++);
      System.out.println(++x);
       // post decrement and pre decrement
      int y=50;
      System.out.println(y--);
      System.out.println(--y);
      
      
  } 
}
